    <h3>When you give blood</h3><hr><br>
    <h4>This is what will happen when you give blood. The whole process takes about an hour. </h4><br>
    <p>
    <ol>
      <li>Welcome and preparation:</li>
      <p>Remember to bring your completed donation safety check form with you, if you received one from us in the post. We will ask you to read our donor consent booklet which explains the importance of blood safety. It’s important to read this whenever you attend because advice does change. We will give you 500ml of fluid just before you give blood. Drinking this over about 5 minutes will help with your well-being during and after donation.</p><br>
      <li>Health screening:</li><ul>
      <li>They make sure it is safe for you to donate, and that your blood donation is safe for a patient to receive.</li>
      
        <li>They confirm your identity and ask you about the information on your donor health check form; a registered nurse may follow up if necessary.</li>
        <li>They test a drop of blood from your finger to check the iron levels (haemoglobin) in your blood.</li>
        <p>If you are not able to donate we will explain why, and may ask you to make another appointment.
          <br>
          If you are able to donate you will be asked to sit in a waiting area until you are called to a donation chair.
        </p>
      </ul><br>
      <li>Your blood donation:</li>
      When you are comfortable on the chair They will ask you to confirm your name, address and date of birth.
      <br>
      They examine your arm and place a cuff on your arm to maintain a small amount of pressure during donation (this does not measure blood pressure).
      <br>
      They then examine your arm to find a suitable vein and clean it with an antiseptic sponge. If we're unable to draw blood first time, we will not make a second attempt on a less suitable vein - this is to keep all our donors safe.
      <br>
      <br>
      <h4>
      They will insert a needle in your arm which will collect your blood into a blood bag with your unique donor number.
        You should not feel any discomfort or pain. If you do, tell a member of staff.
        A scale weighs the blood and stops when you have donated 470ml (or just under a pint). This usually takes between 5-10 minutes.
        The needle will be removed, and a sterile dressing applied to your arm.
</h4>
      </p>
